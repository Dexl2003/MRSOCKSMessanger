package org.example.messengermrsocks.model;

public class MetaData {
    int G;
    int n;
    String openKey;

    public int getG() {
        return G;
    }

    public void setG(int g) {
        G = g;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public String getOpenKey() {
        return openKey;
    }

    public void setOpenKey(String openKey) {
        this.openKey = openKey;
    }
}
